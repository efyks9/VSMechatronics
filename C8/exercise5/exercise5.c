#include <stdio.h>

int main ()
{
    int i;
    printf ( "Type an integer\n" );
    scanf ( "%d",&i );
    if ( i = 0 )
        printf ( "\nBlack\n\n" );
    else if ( i = 1 )
        printf ( "\nBrown\n\n" );
    else if ( i = 2 )
        printf ( "\nRed\n\n" );
    else if ( i = 3 )
        printf ( "\nOrange\n\n" );
    else if ( i = 4 )
        printf ( "\nYellow\n\n" );
    else if ( i = 5 )
        printf ( "\nGreen\n\n" );
    else if ( i = 6 )
        printf ( "\nBlue\n\n" );
    else if ( i = 7 )
        printf ( "\nViolet\n\n" );
    else if ( i = 8 )
        printf ( "\nGrey\n\n" );
    else if ( i = 9 )
        printf ( "\nWhite\n\n" );
    else
        printf ( "\nInvalid input\n\n" );
}